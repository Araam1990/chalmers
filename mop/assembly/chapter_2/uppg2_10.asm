start:
	LDR	R0,=0x55555555
	LDR	R1,=0x40020C00
	STR	R0,[R1]
	LDR	R4,=segcodes
	LDR	R5,=0x40020C14
	LDR	R6,=0x40021010
main:
	LDRB	R0,[R6]
	CMP	R0,#16
	BCC	main_1
	LDRB	R0,=#0
	B	main_2
main_1:
	LDRB	R0,[R4,R0]
main_2:
	STRB	R0,[R5]
	B	main
	

